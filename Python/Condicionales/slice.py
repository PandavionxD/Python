nombre =  "Fransisco"
#Buscamos indices 0
print(nombre[0])
#Buscamos el indice desde el 0 hasta el 3
print(nombre[0:3])
#Buscamos el indice desde el 0 hasta el 3
print(nombre[:3])
#Buscamos desde el 3 hasta el final
print(nombre[3:])
#Buscamos en los indices del 1 hasta el 7
print(nombre[1:7])
#Buscamos desde el indice 1 hasta el 7 pero separando en valores de dos
print(nombre[1:7:2])
#todo el String
print(nombre[::])
#La cadena en valor inverso
print(nombre[::-1])
#Ir en pasos desde el 1 hasta el final en pasos de 3
print(nombre[1::3])
